package atividadesPackage;
public class atividade6 {
public void Boleano(){
 boolean A = true;
 boolean B = false;
 
 if (A == true && B == true) {
	 System.out.println("Os valores de A e B são verdadeiros!");
 } else if (A == true && B == false) {
	 System.out.println("O valor de A é verdadeiro! o de B é falso.");
 } else if (A == false && B == true) {
	 System.out.println("O valor de A é falso! o de B é verdadeiro");
 }else {
	 System.out.println("Os valores de A e B são falsos!");
 }
}
}
